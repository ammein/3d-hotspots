import {
  useEffect,
  useMemo,
  useRef,
  useState,
  memo,
  Suspense,
  useCallback,
} from 'react';
import {
  useGLTF,
  useAnimations,
  PerspectiveCamera,
  Line,
} from '@react-three/drei';
import { useThree } from '@react-three/fiber';
import { GLTFLoader, KTX2Loader } from 'three-stdlib';
